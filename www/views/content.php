       <h3>Basseleur - Het beste voor uw ogen</h3>
<p>
Basseleur Optiek is een gepassioneerde oogspecialist met 80 jaar ervaring in oogzorg en oogmode. Ons specialisme komt tot uitdrukking in een rijkelijke, top-of-the-art monturencollectie. Elke bril selecteren we persoonlijk uit het kwaliteitsaanbod van de betere brillenlabels. Van weelderige oogsieraden tot minimalistische lichtgewicht brillen. Ook in onze zonnebrillencollectie streven we er elk seizoen weer naar onze klanten te verrassen met juweeltjes die u nergens anders ziet.</p>
